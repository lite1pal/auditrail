function normalizeProductResource(input: {
  fields: NonNullable<FrameworkResourceSpec["fields"]>;
  label: string;
  relations?: NonNullable<FrameworkResourceSpec["relations"]>;
  resourceId: string;
}) {
  const pluralPath = pluralizePathSegment(toKebabCase(input.resourceId));

  return frameworkResourceSpecSchema.parse({
    api: {
      filters: [],
      pagination: true,
      prefix: `/v1/organizations/:organizationId/${pluralPath}`,
      public: false
    },
    crud: {
      create: true,
      delete: false,
      list: true,
      read: true,
      update: true
    },
    fields: input.fields,
    label: input.label,
    ownership: "organization",
    permissions: {},
    pluralLabel: pluralizeLabel(input.label),
    relations: input.relations ?? [],
    resource: input.resourceId,
    timestamps: {
      createdAtField: "createdAt",
      enabled: true,
      updatedAtField: "updatedAt"
    },
    ui: {
      createPage: false,
      detailPage: false,
      editPage: false,
      listPage: false,
      nav: false,
      navLabel: pluralizeLabel(input.label)
    }
  });
}

function readProductId(value: string) {
  if (!/^[a-z][a-z0-9-]*$/.test(value)) {
    throw new Error("Generated product ids must be lowercase kebab-case.");
  }

  return value;
}

function readNonEmptyString(value: unknown, field: string) {
  if (typeof value !== "string" || value.trim().length === 0) {
    throw new Error(`Generated product spec field '${field}' must be a non-empty string.`);
  }

  return value.trim();
}

function readOptionalNonEmptyString(value: unknown, field: string) {
  if (value === undefined) {
    return undefined;
  }

  return readNonEmptyString(value, field);
}

function isObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function singularizeProductId(productId: string) {
  if (productId.endsWith("ies")) {
    return `${productId.slice(0, -3)}y`;
  }

  if (productId.endsWith("s")) {
    return productId.slice(0, -1);
  }

  return productId;
}

function pluralizeLabel(value: string) {
  if (value.endsWith("y")) {
    return `${value.slice(0, -1)}ies`;
  }

  if (value.endsWith("s")) {
    return `${value}es`;
  }

  return `${value}s`;
}

function pluralizePathSegment(value: string) {
  if (value.endsWith("y")) {
    return `${value.slice(0, -1)}ies`;
  }

  if (value.endsWith("s")) {
    return `${value}es`;
  }

  return `${value}s`;
}

function toKebabCase(value: string) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/_/g, "-")
    .toLowerCase();
}

function toTitleCase(value: string) {
  return toKebabCase(value)
    .split("-")
    .filter((segment) => segment.length > 0)
    .map((segment) => segment[0]!.toUpperCase() + segment.slice(1))
    .join(" ");
}
