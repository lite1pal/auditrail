import {
  frameworkResourceSpecSchema,
  type FrameworkResourceSpec
} from "../../packages/framework/src/index.js";

export interface GeneratedProductResource {
  listPath: string;
  navLabel: string;
  resource: FrameworkResourceSpec;
}

export interface GeneratedProductSpec {
  description?: string;
  home?: {
    ctaLabel?: string;
    description: string;
    title: string;
  };
  id: string;
  name: string;
  resources: readonly GeneratedProductResource[];
}

export function readGeneratedProductSpec(value: unknown): GeneratedProductSpec {
  if (!isObject(value)) {
    throw new Error("Generated product spec must be a JSON object.");
  }

  const id = readNonEmptyString(value.id, "id");
  const name = readNonEmptyString(value.name, "name");
  const description = readOptionalNonEmptyString(value.description, "description");
  const home = readHome(value.home);
  const resourcesValue = value.resources;

  if (!Array.isArray(resourcesValue) || resourcesValue.length === 0) {
    throw new Error("Generated product spec must include at least one resource.");
  }

  const resources = resourcesValue.map((resourceEntry, index) =>
    readGeneratedProductResource(resourceEntry, index)
  );
  const seenResourceIds = new Set<string>();
  const seenPaths = new Set<string>();

  for (const resourceEntry of resources) {
    if (seenResourceIds.has(resourceEntry.resource.resource)) {
      throw new Error(
        `Generated product spec contains duplicate resource '${resourceEntry.resource.resource}'.`
      );
    }

    if (seenPaths.has(resourceEntry.listPath)) {
      throw new Error(
        `Generated product spec contains duplicate route '${resourceEntry.listPath}'.`
      );
    }

    seenResourceIds.add(resourceEntry.resource.resource);
    seenPaths.add(resourceEntry.listPath);
  }

  return {
    description,
    home,
    id: readProductId(id),
    name,
    resources
  };
}

export function createTodoProductSpec(input: {
  description?: string;
  productId: string;
  productName: string;
}) {
  const resource = normalizeTodoResource(input.productId);

  return readGeneratedProductSpec({
    description:
      input.description ??
      `${input.productName} is a simple resource-backed workspace product generated by Elioric.`,
    home: {
      ctaLabel: `Open ${resource.pluralLabel}`,
      description: `Track work through one generated ${resource.label.toLowerCase()} resource without hand-editing product runtime seams.`,
      title: `${input.productName} workspace`
    },
    id: input.productId,
    name: input.productName,
    resources: [
      {
        listPath: `/${input.productId}/${pluralizePathSegment(toKebabCase(resource.resource))}`,
        navLabel: resource.pluralLabel,
        resource
      }
    ]
  });
}

export function createCrmProductSpec(input: {
  description?: string;
  productId: string;
  productName: string;
}) {
  const resources = createCrmResources(input.productId);

  return readGeneratedProductSpec({
    description:
      input.description ??
      `${input.productName} is a generated CRM workspace product for tracking companies, contacts, deals, and notes through Elioric's reusable product seams.`,
    home: {
      ctaLabel: "Open Deals",
      description:
        "Manage companies, contacts, deals, and notes through one generated multi-resource CRM workspace without hand-editing shared runtime seams.",
      title: `${input.productName} workspace`
    },
    id: input.productId,
    name: input.productName,
    resources: resources.map((resource) => ({
      listPath: `/${input.productId}/${pluralizePathSegment(toKebabCase(resource.resource))}`,
      navLabel: resource.pluralLabel,
      resource
    }))
  });
}

function readGeneratedProductResource(value: unknown, index: number): GeneratedProductResource {
  if (!isObject(value)) {
    throw new Error(`Generated product resource at index ${index} must be an object.`);
  }

  return {
    listPath: readNonEmptyString(value.listPath, `resources[${index}].listPath`),
    navLabel: readNonEmptyString(value.navLabel, `resources[${index}].navLabel`),
    resource: frameworkResourceSpecSchema.parse(value.resource)
  };
}

function readHome(value: unknown) {
  if (value === undefined) {
    return undefined;
  }

  if (!isObject(value)) {
    throw new Error("Generated product home config must be an object.");
  }

  return {
    ctaLabel: readOptionalNonEmptyString(value.ctaLabel, "home.ctaLabel"),
    description: readNonEmptyString(value.description, "home.description"),
    title: readNonEmptyString(value.title, "home.title")
  };
}

function normalizeTodoResource(productId: string): FrameworkResourceSpec {
  const resourceId = singularizeProductId(productId);
  const pluralPath = pluralizePathSegment(toKebabCase(resourceId));

  return frameworkResourceSpecSchema.parse({
    api: {
      filters: [],
      pagination: true,
      prefix: `/v1/organizations/:organizationId/${pluralPath}`,
      public: false
    },
    crud: {
      create: true,
      delete: true,
      list: true,
      read: true,
      update: true
    },
    fields: [
      {
        name: "title",
        required: true,
        searchable: true,
        sortable: true,
        type: "string"
      },
      {
        name: "details",
        required: false,
        type: "text"
      },
      {
        default: "todo",
        name: "status",
        required: true,
        type: "enum",
        values: ["todo", "done"]
      },
      {
        name: "dueAt",
        required: false,
        type: "datetime"
      }
    ],
    label: toTitleCase(resourceId),
    ownership: "organization",
    permissions: {},
    pluralLabel: pluralizeLabel(toTitleCase(resourceId)),
    relations: [],
    resource: resourceId,
    timestamps: {
      createdAtField: "createdAt",
      enabled: true,
      updatedAtField: "updatedAt"
    },
    ui: {
      createPage: false,
      detailPage: false,
      editPage: false,
      listPage: false,
      nav: false,
      navLabel: pluralizeLabel(toTitleCase(resourceId))
    }
  });
}

